package com.nmt.kancollemanager;

/**
 * ExpeditionWorkingData
 */
public class ExpeditionWorkingData {
    private int index;
    private int fleetNo;
    private String name;
    private long time;

    public int getIndex() {
        return index;
    }

    public void setIndex(int index) {
        this.index = index;
    }

    public int getFleetNo() {
        return fleetNo;
    }

    public void setFleetNo(int fleetNo) {
        this.fleetNo = fleetNo;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

    public long getTime() {
        return time;
    }

    public void setTime(long time) {
        this.time = time;
    }
}
